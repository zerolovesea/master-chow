import base64
from openai import OpenAI

client = OpenAI()

def encode_image(image_path):
    with open(image_path, "rb") as image_file:
        return base64.b64encode(image_file.read()).decode("utf-8")


def generate_new_line(base64_image):
    return [
        {
            "role": "user",
            "content": [
                {"type": "text", "text": "Describe this image"},
                {
                    "type": "image_url",
                    "image_url": {
                        "url": f"data:image/jpeg;base64,{base64_image}"
                        },
                },
            ],
        },
    ]

def analyze_image(base64_image, script):
    response = client.chat.completions.create(
        model="gpt-4o",
        messages=[
            {
                "role": "system",
                "content": """
                You are a master of Chinese traditional mysticism and culture: Master Chow. You excel at predicting people's daily luck by observing them and using humorous language. 
                Now, your friend has shown you a selfie, and you need to use your magic to predict his fortune for today.
                """,
            },
        ]
        + script
        + generate_new_line(base64_image),
        max_tokens=500,
    )

    response_text = response.choices[0].message.content
    return response_text
